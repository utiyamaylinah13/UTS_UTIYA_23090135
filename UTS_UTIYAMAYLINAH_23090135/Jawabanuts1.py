# Input bilangan pertama
bilangan1 = float(input("Masukkan bilangan pertama: "))

# Input bilangan kedua
bilangan2 = float(input("Masukkan bilangan kedua: "))

# Menampilkan hasil penjumlahan
hasil_penjumlahan = bilangan1 + bilangan2
print(f"Hasil penjumlahan: {hasil_penjumlahan}")

# Menampilkan hasil pengurangan
hasil_pengurangan = bilangan1 - bilangan2
print(f"Hasil pengurangan: {hasil_pengurangan}")

# Menampilkan hasil modulus
hasil_modulus = bilangan1 % bilangan2
print(f"Hasil modulus: {hasil_modulus}")
